"""Harvest GUI-tuned cosmetics from ../clock.kicad_sch into cosmetics.py.

Workflow: hand-tune symbol positions / ref+value text placement in eeschema,
save, then run  `python3 harvest.py`  to regenerate cosmetics.py.  The next
`build.py` reproduces the tuned look (wires are still drawn by the b_*.py
code, so rewires around a MOVED symbol may need block-code fixes — lint
flags them).

Harvested per user-placed symbol (skips #PWR/#FLG — power glyphs derive from
their parent part):  at (x, y, rot, mirror), Reference/Value text position,
rotation and justify.
"""
from __future__ import annotations
import os
import sys

sys.path.insert(0, os.path.dirname(__file__))
from sexp import parse  # noqa: E402

SRC = os.path.abspath(os.path.join(os.path.dirname(__file__),
                                   "../clock.kicad_sch"))
OUT = os.path.join(os.path.dirname(__file__), "cosmetics.py")


def harvest(path):
    root = parse(open(path).read())
    entries = {}
    for sym in root.findall("symbol"):
        at = sym.find("at")
        x, y = float(at[1]), float(at[2])
        rot = float(at[3]) if len(at) > 3 else 0.0
        mir = str(sym.find("mirror")[1]) if sym.find("mirror") else None
        ref = None
        props = {}
        for p in sym.findall("property"):
            nm = str(p[1])
            if nm == "Reference":
                ref = str(p[2])
            if nm not in ("Reference", "Value"):
                continue
            pat = p.find("at")
            px, py = float(pat[1]), float(pat[2])
            prot = float(pat[3]) if len(pat) > 3 else 0.0
            just = None
            eff = p.find("effects")
            if eff is not None:
                j = eff.find("justify")
                if j is not None:
                    just = " ".join(str(a) for a in j[1:])
            props[nm] = (px, py, prot, just)
        if ref is None or ref.startswith(("#PWR", "#FLG")):
            continue
        entries[ref] = dict(at=(x, y, rot, mir),
                            ref=props.get("Reference"),
                            val=props.get("Value"))
    return entries


def main():
    entries = harvest(SRC)
    with open(OUT, "w") as f:
        f.write('"""GUI-harvested cosmetic overrides (generated by '
                'harvest.py — do not hand-edit).\n'
                'Keyed by reference: at=(x,y,rot,mirror), '
                'ref/val=(x,y,rot,justify) absolute.\n"""\n\n')
        f.write("COSMETICS = {\n")
        for ref in sorted(entries):
            e = entries[ref]
            f.write(f"    {ref!r}: {{'at': {e['at']!r},\n"
                    f"     {'':10}'ref': {e['ref']!r},\n"
                    f"     {'':10}'val': {e['val']!r}}},\n")
        f.write("}\n")
    print(f"wrote {OUT}: {len(entries)} symbols")


if __name__ == "__main__":
    main()
